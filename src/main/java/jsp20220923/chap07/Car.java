package jsp20220923.chap07;

public class Car {
	private String model;
	private Integer price;
	
	
	public Car() {
		
	}


	public String getModel() {
		return model;
	}


	public void setModel(String model) {
		this.model = model;
	}


	public Integer getPrice() {
		return price;
	}


	public void setPrice(Integer price) {
		this.price = price;
	}
	
}
